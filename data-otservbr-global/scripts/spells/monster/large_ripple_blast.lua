local combats = {}

for _, area in ipairs(areas) do
	local notify = Combat()
	local combat = Combat()

	function notifyOnTargetTile(creature, position)
		position:sendMagicEffect(CONST_ME_HITAREA)
		return true
	end

	function onTargetTile(creature, position)
		local tile = Tile(position)
		local creatures = tile:getCreatures()
		if creatures and #creatures > 0 then
			for _, c in pairs(creatures) do
				local player = Player(c)
				if player then
					local multiplier = 1

					local min = player:getMaxHealth() * 0.6 * multiplier
					local max = player:getMaxHealth() * 1.2 * multiplier
					doTargetCombatHealth(creature, player, COMBAT_DEATHDAMAGE, -min, -max, CONST_ME_NONE)
				end
			end
		end

		position:sendMagicEffect(CONST_ME_MORTAREA)
		return true
	end

	combat:setArea(createCombatArea(area))
	notify:setArea(createCombatArea(area))

	combat:setCallback(CALLBACK_PARAM_TARGETTILE, "onTargetTile")
	notify:setCallback(CALLBACK_PARAM_TARGETTILE, "notifyOnTargetTile")

	table.insert(combats, {
		notify = notify,
		combat = combat,
	})
end

local function delayedCastSpell(combat, cid, var, speedChange)
	local creature = Creature(cid)

	if not creature then
		return true
	end
	if speedChange then
		creature:changeSpeed(speedChange)
	end

	return combat:execute(creature, positionToVariant(creature:getPosition()))
end

function spell.onCastSpell(creature, var)
	local cid = creature:getId()

	creature:say("Blast incoming!!!", TALKTYPE_MONSTER_YELL)

	local baseSpeed = creature:getBaseSpeed()
	local combat = combats[math.random(#combats)]
	addEvent(delayedCastSpell, 1000, combat.notify, cid, var, -baseSpeed)
	addEvent(delayedCastSpell, 4000, combat.combat, cid, var, baseSpeed)
	return true
end

